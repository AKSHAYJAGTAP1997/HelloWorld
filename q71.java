


import java.util.Scanner;
class ternary
{
	public static void main(String args[])
	{
		int a=1;
		int b=2;
		
	int max=(a>b)?a:b;
	System.out.println("MAXIMUM= "+max);
	
	
		
		int c=(a<b)?(a+b):(a-b);
		System.out.println("OUTPUT IS...="+c);
		
		
		boolean b=(a<b)?true:false;
		System.out.println("BOOLEAN="+b);
	}
}